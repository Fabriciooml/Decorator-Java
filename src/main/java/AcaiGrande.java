public class AcaiGrande implements Acai{
    public float valor;

    public AcaiGrande() {
    }

    public AcaiGrande(float valor) {
        this.valor = valor;
    }

    public float getValor() {
        return valor;
    }

    public String getComplementos() {
        return "Açaí Grande";
    }
}
